    public void loadProducts() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `ingredient` INNER JOIN unit ON ingredient.unit_id = unit.id");
            
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("unit.name"));
                
                dtm.addRow(v);
            }
            
            jTable1.setModel(dtm);
        } catch (Exception e) {
        }
    }

    public void loadUnits() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `unit`");

            Vector<String> v = new Vector<>();
            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcbm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcbm);

        } catch (Exception e) {
        }

    }

