    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String unit = jComboBox1.getSelectedItem().toString();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (unit.equals("SELECT")) {
            JOptionPane.showMessageDialog(this, "Please select unit", "Message", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `unit` WHERE `name` = '" + unit + "'");
                rs.next();
                String unitId = rs.getString("id");
                
              ResultSet rs2 =  MySQL.search("SELECT * FROM `ingredient` WHERE `name` = '"+name+"'");
              if(rs2.next()) {
                  JOptionPane.showConfirmDialog(this, "Product already added", "Message", JOptionPane.WARNING_MESSAGE);
              }else {
                  
                  MySQL.iud("INSERT INTO `ingredient`(`name`, `unit_id`) VALUES ('"+name+"', '"+unitId+"')");
                  
                  loadProducts();
                  jTextField1.setText("");
                  jComboBox1.setSelectedIndex(0);
                  
                  JOptionPane.showMessageDialog(this, "Product added", "Message", JOptionPane.INFORMATION_MESSAGE);
                  
              }
            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

